package dec28;

public class ForLoop {

	public static void main(String[] args) {
		for (int i = 1; i <=5; i++) 
		{
			System.out.println("Naresh::"+i);	
		}
		System.out.println("========");
		for (int i = 1; i <=5; i+=3) 
		{
			System.out.println("Naresh:"+i);	
		}
		System.out.println("============");
		for (int i = 10; i >=1; i--) 
		{
			System.out.println("Naresh:"+i);	
		}
	}

}
