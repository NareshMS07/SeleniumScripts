package dec26;

public class ToUpper_Lowercase {

public static void main(String[] args) {
String course="QTP";
switch ("course.toUpperCase") {
case "QTP":
System.out.println("course is available");
break;
case "MANUAL":
	System.out.println("course is available");
break;
default:
System.out.println("course is not available");
	break;
}
	}
}
